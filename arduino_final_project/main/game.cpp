static uint8_t lfsrRandomDirection() {
    static uint16_t state = 0xACE1;

    uint16_t bit = state & 1;
    state >>= 1;
    if (bit != 0) {
        state ^= 0xB400;
    }

    return (uint8_t)(state & 0x03);
}

